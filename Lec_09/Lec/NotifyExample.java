public synchronized void deposit(double amount) {
    this.notify(); // този метод изпраща сигнал за събуждане към една от нишките, които чакат на същищ this обект
    this.balance += amount; 
    // едва когато се излиза от този метод, се отключва this ключалката и някоя събудена нишка може да се опита да вземе ключалката, за да провери условието в while цикъла
}